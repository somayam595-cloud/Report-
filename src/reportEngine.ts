function numbered(items: string[]): string {
  return items.map((item, index) => `${index + 1}. ${item}`).join('\n');
}

export function buildSmartDraft(programName: string): SmartDraft {
  const normalized = programName.trim().toLowerCase();
  const template = templates.find((candidate) => candidate.test.test(normalized)) ?? defaultTemplate;
  const name = programName.trim() || 'البرنامج المدرسي';

  return {
    field: template.field,
    beneficiaries: template.beneficiaries,
    beneficiaryCount: template.count,
    goals: numbered(template.goals.map((goal) => goal.replace('البرنامج', `برنامج «${name}»`))),
    steps: numbered(template.steps),
    outcomes: numbered(template.outcomes),
    recommendations: numbered(template.recommendations),
  };
}
